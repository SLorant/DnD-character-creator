<pre>
The aasimar bear the legacy of a celestial
being or even a deity in their ancestry,
and have incredible potential to do good in the
world. At the same time, their heritage marks
them as different and often leads to
persecution, ridicule, or exile from superstitious
or backward communities. It is not unknown for
an aasimar to give in to bitterness in the face of
adversity and turn to evil.

Aasimar are the descendants of humans and
some good outsider, such as a true celestial,
a celestial creature, couatl, lillend, or even
a servant or avatar of a good deity. (Some of
these creatures must use magic to assume
a form that is compatible with a human mate,
of course.) While elves, dwarves, gnomes,
and halflings with good outsider ancestry are
reputed to exist, those crossbreeds are not true
aasimar. Aasimar look human except for one
distinguishing feature related to their unusual
ancestor. Some examples of these features
(and the ancestors that cause them) are:
• golden eyes
• silver hair
• emerald skin (planetar)
• feathers at the shoulder (astral deva, avoral
celestial, planetar, solar, trumpet archon)
• feathers in hair (avoral celestial)
• pearly opalescent eyes (ghaele celestial)
• powerful ringing voice (lillend, trumpet archon)
• brilliant topaz eyes (solar)
• silvery or golden skin (solar)
• iridescent scales in small patches (couatl or
lillend)

Aasimar understand that they are special, even
if they do not understand their true heritage.
Many aasimar from a latent bloodline don’t
even know what creature engendered the line
in the first place. Two aasimar from the same
bloodline often have the same distinguishing
feature. 
</pre>

